check_port() {
	local port="$1"
	if netstat -tnlp | grep -q "$port"; then
		echo "$port 运行中"
	else
		echo "$port 未运行"
	fi
}

ports=("33112" "33113" "33115" "33116" "33117" "33118" "36003" "36006" "36007" "36008" "30002" "30003" "30004" "30006" "30007" "31001" "31002" "8062" "30002")

for port in "${ports[@]}"; do
	check_port "$port"
done
